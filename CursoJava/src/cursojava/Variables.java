package cursojava;

//int=numeros enteros
// long = almacenas mas numeros enteros que int
// float = almacena decimales de 6 a 7 digitos (Se agrega una F al final)
//double = el mas utilizado 15 digitos decimales almacena
// char = solo almacena un caracter 'A' ,etc
// boolean = almacena true o false
// String = para almacenas varias letras
public class Variables {
    public static void main(String[] args) {
        
        long numeroGrande=555555555;
        System.out.println(numeroGrande);
        float nd=3.5f;
        System.out.println(nd);
        double deci=3.555;
        System.out.println(deci);
        char Abc='A';
        System.out.println(Abc);
        boolean op=true;
        op=false;
        System.out.println(op);
        String Nombre="Ronald";
        System.out.println(Nombre);
     
        
        
        
        
        
        
    }
    
}
